  static String getText(
    String key,
    String languageCode, {
    List<dynamic>? args,
  }) {
    String text = _localizedValues[languageCode]?[key] ?? key;

    if (args != null && args.isNotEmpty) {
      for (var i = 0; i < args.length; i++) {
        text = text.replaceAll('%${i + 1}', args[i].toString());
        // Support both indexed and non-indexed placeholders
        text = text.replaceFirst('%d', args[i].toString());
        text = text.replaceFirst('%s', args[i].toString());
      }
    }

    return text;
  }

